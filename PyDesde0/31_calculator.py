def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a, b):
    return a / b

def exp(a, b):
    return a ** b

print(add(3, 5))

print(subtract(10, 5))

print(multiply(2, 10))

print(divide(10, 2))

print(exp(10, 2))